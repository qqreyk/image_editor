#pragma once

#include "image.h"

/**
 * @brief Пространство имён для операций обработки изображений.
 */
namespace filters {

/**
 * @brief Преобразует изображение в чёрно-белое (оттенки серого).
 *
 * Использует формулу взвешенной яркости:
 * Y = 0.299*R + 0.587*G + 0.114*B.
 * Значение Y присваивается всем RGB-каналам; альфа-канал не изменяется.
 *
 * @param img Изображение для преобразования (изменяется на месте).
 * @throws std::invalid_argument Если у изображения меньше 3 каналов.
 */
void toGrayscale(Image& img);

/**
 * @brief Изменяет яркость изображения.
 *
 * Прибавляет @p delta к каждому из RGB-каналов с ограничением в [0, 255].
 * Альфа-канал не изменяется.
 *
 * @param img   Изображение для обработки.
 * @param delta Изменение яркости: положительное значение увеличивает яркость, отрицательное
 * уменьшает.
 */
void adjustBrightness(Image& img, int delta);

/**
 * @brief Накладывает цветовой фильтр (тинт) на изображение.
 *
 * Умножает каждый канал на соответствующий коэффициент фильтра:
 * R_new = R * filterR / 255, и так для G, B.
 *
 * @param img     Изображение для обработки.
 * @param filterR Интенсивность красного фильтра [0, 255].
 * @param filterG Интенсивность зелёного фильтра [0, 255].
 * @param filterB Интенсивность синего фильтра [0, 255].
 * @throws std::invalid_argument Если значения фильтра вне диапазона [0, 255].
 */
void applyColorFilter(Image& img, int filterR, int filterG, int filterB);

/**
 * @brief Размывает изображение (box blur 3×3).
 *
 * Каждый пиксель заменяется средним арифметическим своих соседей в окне 3×3.
 * Пиксели у краёв используют только доступных соседей (не выходят за границы).
 *
 * @param img Изображение для размытия.
 */
void blur(Image& img);

/**
 * @brief Изменяет размер изображения на целое кратное.
 *
 * При @p factor > 0 увеличивает: каждый пиксель растягивается в блок factor×factor.
 * При @p factor < 0 уменьшает: каждый блок |factor|×|factor| усредняется в один пиксель.
 *
 * @param img    Исходное изображение.
 * @param factor Кратность изменения (не может быть 0 или ±1 для масштабирования).
 * @return Новое изображение изменённого размера.
 * @throws std::invalid_argument Если factor равен 0.
 * @throws std::invalid_argument Если изображение нельзя равномерно уменьшить на данный factor.
 */
Image resizeImage(const Image& img, int factor);

/**
 * @brief Инвертирует цвета изображения.
 *
 * Для каждого RGB-канала: new_value = 255 - old_value.
 * Альфа-канал не изменяется.
 *
 * @param img Изображение для инверсии.
 */
void invertColors(Image& img);

/**
 * @brief Повышает резкость изображения (unsharp-mask ядро 3×3).
 *
 * Применяет свёрточное ядро:
 * @code
 *  0  -1   0
 * -1   5  -1
 *  0  -1   0
 * @endcode
 * Результат ограничивается в [0, 255]. Пиксели по краям не изменяются.
 *
 * @param img Изображение для повышения резкости.
 */
void sharpen(Image& img);

/**
 * @brief Поворачивает изображение на 90° по часовой стрелке.
 *
 * Новые размеры: ширина = исходная высота, высота = исходная ширина.
 * Формула: dst[x][y] = src[y][width-1-x].
 *
 * @param img Исходное изображение.
 * @return Новое повёрнутое изображение.
 */
Image rotate90(const Image& img);

}  // namespace filters
